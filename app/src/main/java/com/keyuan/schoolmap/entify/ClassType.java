package com.keyuan.schoolmap.entify;

import java.util.List;

/**
 * Created by Administrator on 2017/10/20.
 */

public class ClassType {
    private List<CourseCategory> course_category;

    public static class CourseCategory {
        private String uuid;
        private String name;
        private int sort;

        public String getUuid() {
            return uuid;
        }

        public void setUuid(String uuid) {
            this.uuid = uuid;
        }

        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }

        public int getSort() {
            return sort;
        }

        public void setSort(int sort) {
            this.sort = sort;
        }
    }

    public List<CourseCategory> getCourse_category() {
        return course_category;
    }

    public void setCourse_category(List<CourseCategory> course_category) {
        this.course_category = course_category;
    }
}
