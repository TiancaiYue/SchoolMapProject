package com.keyuan.schoolmap.entify;

import java.util.List;

/**
 * Created by Administrator on 2017/10/20.
 */

public class HotCourse {
    private CourseInfo course_info;

    public CourseInfo getCourse_info() {
        return course_info;
    }

    public void setCourse_info(CourseInfo course_info) {
        this.course_info = course_info;
    }

    public static class CourseInfo {
        private List<Rows> rows;
        private int recordCount = 0;

        public List<Rows> getRows() {
            return rows;
        }

        public void setRows(List<Rows> rows) {
            this.rows = rows;
        }

        public int getRecordCount() {
            return recordCount;
        }

        public void setRecordCount(int recordCount) {
            this.recordCount = recordCount;
        }

        public static class Rows {
            private String uuid = "";
            private String title = "";
            private String show_pics = "";
            private String course_price;
            private String pack_price;
            private String commission_points;
            private String release_time = "";

            public String getPack_price() {
                return pack_price;
            }

            public void setPack_price(String pack_price) {
                this.pack_price = pack_price;
            }

            public String getUuid() {
                return uuid;
            }

            public void setUuid(String uuid) {
                this.uuid = uuid;
            }

            public String getTitle() {
                return title;
            }

            public void setTitle(String title) {
                this.title = title;
            }

            public String getShow_pics() {
                return show_pics;
            }

            public void setShow_pics(String show_pics) {
                this.show_pics = show_pics;
            }

            public String getCourse_price() {
                return course_price;
            }

            public void setCourse_price(String course_price) {
                this.course_price = course_price;
            }

            public String getCommission_points() {
                return commission_points;
            }

            public void setCommission_points(String commission_points) {
                this.commission_points = commission_points;
            }

            public String getRelease_time() {
                return release_time;
            }

            public void setRelease_time(String release_time) {
                this.release_time = release_time;
            }
        }
    }
}
